package builtInMethods;

public class concatMethod {
	// concat() is used to concatinate 2 strings
	// concat() returns a new concatinated string
	public static void main(String[] args) {
		String s1 = "Amrita";
		String s2 = "Deb";
		System.out.println(s1.concat(s2));
		System.out.println("Soumen".concat("Dey"));
	}

}
