package datatypes;

/*
 * Datatypes are used to create block of memory which stores data,
 * In java, there are 2 types of datatypes
 * i) Primitive, ii) Non-primitive
 * */

public class Primitive {
	
	/*
	 * There are 8 primitive datatypes in java
	 * 1) boolean - 1bit > false  <<default value>>
	 * 2) byte - 1 byte > 0
	 * 3) short - 2 bytes > 0
	 * 4) int - 4 bytes > 0
	 * 5) long - 8 bytes > 0l
	 * 6) float - 4 bytes > 0.0f
	 * 7) double - 8 bytes > 0.0d
	 * 8) char - 2 bytes > \u0000
	 * */

}
