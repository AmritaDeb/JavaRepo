package string;

public class StringComparisonUsingCompareTo {
	
	// compareTo() is used to compare 2 strings
	// compareTo() is actually belongs to Comparable interface
	// String class implements Comparable interface, 
	// String class provides implementation for compareTo()
	
	public static void main(String[] args) {
		
		// comapreTo() returns 0 if both the strings are same
		System.out.println("a".compareTo("a"));  	// 0 
		
		// compareTo() returns integer value if both the strings are not same
		// 1. If the LHS string is greater than the string given as argument then compareTo() returns a negative integer
		System.out.println("A".compareTo("a"));		// -32
		System.out.println("A".compareTo("b"));		// -33
		
		// 2. If the LHS string is lesser than the string given as argument then compareTo() returns a positive integer
		System.out.println("a".compareTo("A"));		// 32
		System.out.println("a".compareTo("B"));		// 31
	}

}
